	/**
	 * 引数(A)で受け取った配列の要素を表示するメソッド
	 *
	 * @param msg
	 * @param array
	 */
	private static void outInArray(String msg, int[] A) {
		String buff = "";
		for (int i = 0; i < A.length; i++) {
			// 三項演算子を用いることで、,の表示をシンプルに記述できます。
			buff += (i == A.length - 1) ? A[i]:A[i] + ",";
		}
		System.out.println(msg + ":\n" + buff);
	}
