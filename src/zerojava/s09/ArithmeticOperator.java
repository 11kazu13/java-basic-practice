package zerojava.s09;

public class ArithmeticOperator {
	public static void main(String[] args) {
		int x = 20;
		int y = 8;
		
		System.out.print("x = ");
		System.out.println(x);
		System.out.print("y = ");
		System.out.println(y);
		
		System.out.print("x + y = ");
		System.out.println(x + y);
		
		System.out.print("x - y = ");
		System.out.println(x - y);
		
		System.out.print("x * y = ");
		System.out.println(x * y);
		
		System.out.print("x / y = ");
		System.out.println(x / y);
		
		System.out.print("x % y = ");
		System.out.println(x % y);
		
	}

}
