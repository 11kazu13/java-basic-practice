package zerojava.s04;

public class VariableAssignment {
	public static void main(String[] args) {
		String name;
		int age;
		
		name = "Java太郎";
		age = 33;
		
		System.out.print(name);
		System.out.println("です。");
		System.out.print(age);
		System.out.println("歳です。");
		
		name = "Python太郎";
		age = 27;
		
		System.out.print(name);
		System.out.println("です。");
		System.out.print(age);
		System.out.println("歳です。");
	}

}
