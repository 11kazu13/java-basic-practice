package zerojava.s17;

public class ArrayPattern1 {
	public static void main(String[] args) {
		String cards[];
		cards = new String[3];
		
		cards[0] = "レア";
		cards[1] = "スーパーレア";
		cards[2] = "ウルトラレア";
		
		System.out.println(cards[0]);
		System.out.println(cards[1]);
		System.out.println(cards[2]);
	}

}
